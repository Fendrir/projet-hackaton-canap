            <h4>Pourquoi faire du coworking ?</h4>
            <p>Le coworking, cotravail en français
            ou parfois bureaux partagés est un type d'organisation du 
            travail qui regroupe deux notions : un espace de travail partagé,
            mais aussi un réseau de travailleurs encourageant l'échange et l'ouverture. 
            Il est un des domaines de l'économie collaborative.
            Cela permet aux travailleurs du numérique d'échanger des procédés et des 
            compétences de travail voire même des méthodes. Le co-working est au numérique ce que
            le principe d'échange équivalent est à l'alchimie.</p>
